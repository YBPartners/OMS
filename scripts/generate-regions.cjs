for (const r of regions) {
  const key = `${r.sido}|${r.sigungu}`;
  if (!sigungus.has(key)) {
    sigungus.set(key, { sido: r.sido, sigungu: r.sigungu, ac: r.ac, emd: r.emd });
  }
}

